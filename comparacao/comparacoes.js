// == comparação implícita (compara só o valor)

const numero = 5;
const texto = '5';
// faz a cnversão
console.log(numero == texto);

// === comparação explícita (compara o valor o tipo de dado)

console.log(numero === texto);

// typeof serve para descobrir qual o tipo de dado da variável

console.log(typeof numero);
console.log(typeof numero);

/* as boas práticas recomendam sempre o uso de '==='*/
/* e conversões devem ser feitas sempre de forma explícita com as funções de conversão*/